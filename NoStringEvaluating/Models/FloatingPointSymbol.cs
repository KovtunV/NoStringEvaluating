namespace NoStringEvaluating.Models;

/// <summary>
/// Symbol of floating point
/// </summary>
public enum FloatingPointSymbol
{
    /// <summary>
    /// .
    /// </summary>
    Dot = 0,

    /// <summary>
    /// ,
    /// </summary>
    Comma = 1,

    /// <summary>
    /// . or ,
    /// </summary>
    DotComma = 2,
}
